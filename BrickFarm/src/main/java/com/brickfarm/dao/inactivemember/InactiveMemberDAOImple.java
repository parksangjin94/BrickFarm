package com.brickfarm.dao.inactivemember;

import java.util.List;

import javax.inject.Inject;

import org.apache.ibatis.session.SqlSession;
import org.springframework.stereotype.Repository;

import com.brickfarm.dto.admin.kmh.AdminMemberDTO;
import com.brickfarm.dto.admin.kmh.AdminNoActiveMemberDTO;
import com.brickfarm.vo.admin.kmh.AdminInactiveMemberVO;

@Repository
public class InactiveMemberDAOImple implements InactiveMemberDAO {

	@Inject
	private SqlSession ses;

	private static String ns = "com.brickfarm.mappers.InactiveMemberMapper";

	// ==[송지영]========================================================================================================================================

	// ==================================================================================================================================================

	// ==[김미형]========================================================================================================================================

	@Override
	public List<AdminInactiveMemberVO> selectInactiveMember(AdminNoActiveMemberDTO tmpMember) throws Exception {

		return ses.selectList(ns + ".findInactiveMember", tmpMember);
	}

	@Override
	public int insertInactiveMember(List<Integer> member_no) {
		return ses.insert(ns + ".insertInactiveMember", member_no);
	}

	@Override
	public boolean updateActiveMember(String member_id) throws Exception {
		boolean result = false;
		if (ses.update(ns + ".updateActiveMember", member_id) == 1) {
			result = true;
		}
		return result;
	}

	// ==================================================================================================================================================

	// ==[이경민]========================================================================================================================================

	// ==================================================================================================================================================

	// ==[염세환]========================================================================================================================================

	// ==================================================================================================================================================

	// ==[박상진]========================================================================================================================================

	// ==================================================================================================================================================

	// ==[송영태]========================================================================================================================================

	// ==================================================================================================================================================

	// ==[김용진]========================================================================================================================================

	// ==================================================================================================================================================
}
